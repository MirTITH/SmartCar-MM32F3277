// **************************** 宏定义 ****************************

// **************************** 变量定义 ****************************
uint16 use_time;
// **************************** 变量定义 ****************************

// **************************** 代码区域 ****************************
void delay (uint16 loop)
{
	uint8 a,b;
	for(; loop>0; loop--)
	for(a = 0xff; a>0; a--)
	for(b = 0xff; b>0; b--);
}

int main(void)
{
	board_init(true);																// 初始化 debug 输出串口

	systick_delay_ms(1000);															// 延时
	systick_start();																// 开始计时
	// 将需要测试运行时间的代码放置在这里
	delay(10);																		// 测试代码
	// 将需要测试运行时间的代码放置在这里
	use_time = systick_getval_ms();													// 得到的时间单位为毫秒
	printf("\r\nThe soft delay use %dms.", use_time);								// 输出耗时

	while(1)
	{
	}
}
